package com.syntax.class02;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int num1= 10;
		int num2= 5;
		
		int sum = num1 + num2;
		int sub = num1 - num2;
		int div = num1 / num2;
		int multi = num1 * num2;
		
		
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(div);
		System.out.println(multi);
		
		double d1 = 3.4;
		double d2 = 5.73;
		double dsum = d1 + d2;
		System.out.println(dsum);
		
		// you can store an intiger as double. 
		double dSum2= num1 + num2;
		System.out.println(dSum2);
		
		//order of operation
		System.out.println("Ahmet"+ 10 + 30);
		
		//first parenthesis, then concatenation
		System.out.println("Ahmet_"+ (10 + 3));
		
		//first multiplication then concatenate 
		System.out.println("Ahmetn_" + 10 * 30);
		
		
	//Arthmetic Operation
		//PEMDAS
		
		
	}
	
	
	
	
}
